#include <iostream>
using namespace std;
int main()
{
    int digit;
    cin>>digit;
    if(digit==1)
    cout<<"one"<<endl;
    else if(digit==2)
    cout<<"two"<<endl;
    else if(digit==3)
    cout<<"three"<<endl;
    else if(digit==4)
    cout<<"four"<<endl;
    else if(digit==5)
    cout<<"five"<<endl;
    else if(digit==6)
    cout<<"six"<<endl;
    else
    cout<<"seven"<<endl;
}
